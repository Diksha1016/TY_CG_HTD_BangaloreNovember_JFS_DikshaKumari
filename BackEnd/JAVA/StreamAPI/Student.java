
public class Student {

	String name;
	int age;
	public Student(int i, String string) {
		age=i;
		name=string;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	} 

	

}
